package Chapter02;

public class VariableExample {
	public static void main(String[] args) {
		int name; 		//변수 이름 알파벳으로 시작 가능
		double 넓이;		//변수 이름 한글로도 시작 가능
		int $asdf;		//변수 이름 $로 시작 가능
		int _asdf;		//변수 이름 _로 시작 가능
		
		int a123a;		//변수 이름 숫자로 시작 불가능
		int $123a;		//변수 이름 다른 기호로 시작 불가능
		
	}
}
